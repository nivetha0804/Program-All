package com.StringMethod;

public class EgCharAt 
{
	public static void main(String[] args) 
	{
		/**
		 * charAt(int index):
		 * 
		 * Returns the char value at the specified index.
		 */
		
		String text = "I Like To Travel All Over The World";
		
		System.out.println(text.charAt(0));
		System.out.println(text.charAt(2));
		System.out.println(text.charAt(7));
		System.out.println(text.charAt(10));
		System.out.println(text.charAt(17));
		System.out.println(text.charAt(21));
		System.out.println(text.charAt(26));
		System.out.println(text.charAt(30));


	}
}
