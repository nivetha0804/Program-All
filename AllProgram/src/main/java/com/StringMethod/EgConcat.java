package com.StringMethod;

public class EgConcat 
{
	public static void main(String[] args) 
	{
		/**
		 *concat(String str):
		 *
		 *Concatenates the specified string to the end of this string.
		 */		

		String fname = "S ";

		String lname = "Nivetha";

		System.out.println(fname.concat(lname));
	}
}
