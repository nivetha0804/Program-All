package com.project._02_2022nive;

public class Forloop 
{
	public static void main( String[] args )
	{
		//for loop
		/**
		 * for(initialization; condition; increment/decrement)
		 * {
		 * 		statements;
		 * }
		 */
		for(int n = 2; n <= 10; n++)
		{
			System.out.println(n);
		}
	}
}
